public class ArithmaticOperator {
        public static void main(String[] args) {
            int A = 10;
            int B = 5;
            System.out.println("Add = " + (A+B));   // Binary ArithmaticOperator//

            int C = 100;
            int D = ++C;
            System.out.println(C);
            System.out.println(D);

            int E = 100;
            int F = E++;
            System.out.println(E);
            System.out.println(F);
        }
    
}