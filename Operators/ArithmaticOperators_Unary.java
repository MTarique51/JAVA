 public class ArithmaticOperators_Unary {
    public static void main(String[] args) {

        // Pre Increment //
        int a = 10;
        int b = ++a;
        System.out.println("VALUE ACCORDING TO PRE INCREMENT");
        System.out.println(a);
        System.out.println(b);


        // Post Increment //
        int c = 10;
        int d = c++;
        System.out.println("VALUE ACCORDING TO POST INCREMENT");
        System.out.println(c);
        System.out.println(d);

        // Pre Decrement //
        int e = 10;
        int f = --e;
        System.out.println("VALUE ACCORDING TO PRE DECREMENT");
        System.out.println(e);
        System.out.println(f);

        // Post Decrement //
        int g = 10;
        int h = g--;
        System.out.println("VALUE ACCORDING TO POST DECREMENT");
        System.out.println(g);
        System.out.println(h);
    
    
    }
}
