public class Assignment_Operaors {
    public static void main(String[] args) {

        int A = 10;
        //A = A+10;
        A += 10;
        System.out.println(A);

        int B = 15;
        //B = B-15;
        B -= 15;
        System.out.println(B);

        
        int C = 5;
        //C = C*5;
        C *= 5;
        System.out.println(C);

        
        int D = 20;
        //D = D/4;
        D /= 4;
        System.out.println(D);
    }
}
