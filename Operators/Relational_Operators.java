public class Relational_Operators {
    public static void main(String[] args) {
        int A = 15;
        int B = 10;
        int C = 15;
        System.out.println("According to comparision :" +  (A == B));                     // USE OF == 
        System.out.println("According to Unequal :" + (A != B));                         // USE OF !=
        System.out.println("According to Greater Than :" + (A > B));                     // USE OF < 
        System.out.println("According to Less Than :" + (A < B));                        // USE OF < 
        System.out.println("According to Greater than or Equal :" + (A >= B));           // USE OF == 
        System.out.println("According to Less  than or Equal  :" + (A <= C));            // USE OF == 
    }
}
